         long long unsigned int decimalValue(Node *head)
        {
           // Your Code Here
          long long unsigned int d = 0;

        Node *ptr = head;
//1101          7
        while (ptr != NULL)
        {
            // Left shift the current result by 1 and add the current bit
            d = (d << 1) + ptr->data;
            d = d % MOD;

            ptr = ptr->next;
        }

        return d;
           
        }
